const importData = async () => {
    try {
        await Category.deleteMany();
        await Product.deleteMany();
        // optionally await User.deleteMany(); if you want to reset users too, but usually not needed for product fixes

        await Category.insertMany(categoriesTree);
        await Product.insertMany(products);

        console.log('Data Imported!');
        process.exit();
    } catch (error) {
        console.error(`${error}`);
        process.exit(1);
    }
};

mongoose.connect(MONGODB_URI).then(() => {
    importData();
});
